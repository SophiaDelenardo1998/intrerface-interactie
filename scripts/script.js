var buttonLink = document.querySelector("main>button");
var Link = document.querySelector("main>img");

buttonLink.onclick = gaRennenLink;


/* ben met dit geholpen en snap het nu wel*/
function gaRennenLink(){
    Link.classList.toggle("ikRen");
    if(Link.classList.contains("ikRen")){
        setTimeout(() => {
            Link.classList.remove("ikRen");
        }, 2000);
    }
}

